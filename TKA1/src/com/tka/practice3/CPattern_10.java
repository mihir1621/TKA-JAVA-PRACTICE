package com.tka.practice3;

public class CPattern_10 {
	public static void main(String[] args) {
		
		for(int j = 1;j<=1;j++) {
			if(j==1) {
				System.out.println(1+" ");
			}
			else if(j==2) {
				System.out.println(2+" ");
			}
			else if(j==3) {
				System.out.println(3+" ");
			}
			else if(j==4) {
				System.out.println(4+" ");
			}
			else 
			{
				System.out.println(15+" ");
			}
			System.out.println();
		}
		
	}

}
